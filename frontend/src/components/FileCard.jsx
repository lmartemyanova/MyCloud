import { useState } from "react";
import { extractFilename } from "../utils/extractFilename";
import {
  deleteFile,
  updateComment,
  markAsDownloaded,
  renameFile 
} from "../services/files";
import "../styles/storage.css";
import FilePreview from "./FilePreview";
import { preserveFileExtension } from "../utils/filename";

const FileCard = ({ file, onAction }) => {
    const [editingName, setEditingName] = useState(false);
    const [newName, setNewName] = useState(file.original_name);
    
    const [editingComment, setEditingComment] = useState(false);
    const [newComment, setNewComment] = useState(file.comment || "");

    const [previewOpen, setPreviewOpen] = useState(false);

    const publicUrl = `http://localhost:5173/public/${file.unique_link}`;

  
    const handleDelete = async () => {
      const confirmed = window.confirm("–£–¥–∞–ª–∏—Ç—å —Ñ–∞–π–ª?");
      if (!confirmed) return;
  
      await deleteFile(file.id);
  
      onAction();
    };


    const handleDownload = async () => {
      try {
        const res = await fetch(`http://localhost:8000/api/storage/download/${file.unique_link}/`, {
          headers: {
            Authorization: `Bearer ${localStorage.getItem("token")}`,
          },
        });
    
        if (!res.ok) throw new Error("–û—à–∏–±–∫–∞ —Å–∫–∞—á–∏–≤–∞–Ω–∏—è");
    
        const blob = await res.blob();

        const contentDisposition = res.headers.get("Content-Disposition");
        const filename = extractFilename(contentDisposition, file.original_name || "downloaded-file");
    
        const url = window.URL.createObjectURL(blob);
        const link = document.createElement("a");
        link.href = url;
        link.download = filename;
        link.click();
        window.URL.revokeObjectURL(url);
    
        await markAsDownloaded(file.id);
    
        onAction();
      } catch (err) {
        console.error(err);
        alert("–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —Ñ–∞–π–ª");
      }
    };


    const handleRename = async () => {
      try {
        const fixedName = preserveFileExtension(file.original_name, newName);
        await renameFile(file.id, fixedName);
        setEditingName(false);
        onAction();
      } catch {
        alert("–ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–∏–º–µ–Ω–æ–≤–∞—Ç—å —Ñ–∞–π–ª");
      }
    };


    const handleCommentSave = async () => {
      try {
        await updateComment(file.id, newComment);
          setEditingComment(false);
          onAction();
        } catch {
          alert("–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π");
        }
    };
  

    return (
      <div className="file-card">
        {editingName ? (
          <>
            <input
              type="text"
              value={newName}
              onChange={(e) => setNewName(e.target.value)}
            />
            <button onClick={handleRename}>üíæ –°–æ—Ö—Ä–∞–Ω–∏—Ç—å</button>
            <button
              onClick={() => {
                setEditingName(false);
                setNewName(file.original_name); 
              }}
            >
              ‚úñ
            </button>
          </>
        ) : (
          <h4>
            {file.original_name}{" "}
            <button onClick={() => setEditingName(true)}>‚úèÔ∏è</button>
          </h4>
        )}

        {editingComment ? (
          <>
            <input
              type="text"
              value={newComment}
              onChange={(e) => setNewComment(e.target.value)}
            />
            <button onClick={handleCommentSave}>üíæ –°–æ—Ö—Ä–∞–Ω–∏—Ç—å</button>
            <button
              onClick={() => {
                setEditingComment(false);
                setNewComment(file.comment);
              }}
            >
              ‚úñ
            </button>
          </>
        ) : (
          <p>
            –ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π: {file.comment || "‚Äì"}{" "}
            <button onClick={() => setEditingComment(true)}>‚úèÔ∏è</button>
          </p>
        )}
      
        <p>–†–∞–∑–º–µ—Ä: {(file.size / 1024).toFixed(2)} –ö–ë</p>
        <p>–ó–∞–≥—Ä—É–∂–µ–Ω: {new Date(file.uploaded_at).toLocaleString()}</p>
        <p>–ü–æ—Å–ª–µ–¥–Ω–µ–µ —Å–∫–∞—á–∏–≤–∞–Ω–∏–µ: {file.last_downloaded_at 
          ? new Date(file.last_downloaded_at).toLocaleString() 
          : "‚Äî"}
        </p>
        <div className="actions">
        
          <button onClick={() => setPreviewOpen(true)}>üëÅ –ü—Ä–µ–¥–ø—Ä–æ—Å–º–æ—Ç—Ä</button>
          {previewOpen && (
            <div className="preview-modal">
              <button onClick={() => setPreviewOpen(false)}>‚ùå –ó–∞–∫—Ä—ã—Ç—å</button>
              <FilePreview file={file} isPublic={false} />
            </div>
          )}

          <button onClick={handleDownload}>‚¨áÔ∏è –°–∫–∞—á–∞—Ç—å</button>
          <button onClick={() => navigator.clipboard.writeText(publicUrl)}>üîó –°–∫–æ–ø–∏—Ä–æ–≤–∞—Ç—å —Å—Å—ã–ª–∫—É</button>
          <button onClick={handleDelete}>üóë –£–¥–∞–ª–∏—Ç—å</button>
        </div>
      </div>
    );
  };
  
  export default FileCard;